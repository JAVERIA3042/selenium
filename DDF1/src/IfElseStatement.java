
public class IfElseStatement {

	public static void main(String[] args) {
		
		//Declare Variable
		int a, b;
		
		//initialize Variable
		a = 10;
		b = 20;
		
		System.out.println("varibale a is " + a);
		System.out.println("Varibale b is " + b);
		
		//Condition Statement or Compare Statement
		if(a == b) {
			System.out.println("They are the Same");
		}
		else {
			System.out.println("They are the Different");
		}
			
	}

}
