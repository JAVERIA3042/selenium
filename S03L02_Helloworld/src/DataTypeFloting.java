
public class DataTypeFloting {

	public static void main(String[] args) {
		int a = 9/2;
		float b = 9f;
		double c = 9d;
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		
		
		int marker = 512;
		double percentage = marker * 0.46f;
		System.out.println(percentage);
	}

}
